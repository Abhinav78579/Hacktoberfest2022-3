# Python code to find the largest number
# in a list


def maxelement(lst):
	# displaying largest element
	# one line solution
	print(max(lst))


# driver code
# input list
lst = [20, 10, 20, 4, 100]
# the above input can also be given as
# lst = list(map(int, input().split())) # -> taking input from the user
